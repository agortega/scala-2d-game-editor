package org.brijest.storm.engine
package model






sealed trait Trigger extends Immutable


object NoTrigger extends Trigger


final case class AfterTime(turns: Long) extends Trigger {
  assert(turns > 0)
  assert(turns < (Long.MaxValue - 1))
}


