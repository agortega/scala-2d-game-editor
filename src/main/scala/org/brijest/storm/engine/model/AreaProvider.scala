package org.brijest.storm
package engine
package model



import collection._



trait AreaProvider {
  def name: String
  def acquire(): Area
  def release(a: Area): Unit
}


object AreaProvider {
  
  final class Strict(area: Area) extends AreaProvider {
    def name = "Strict"
    def acquire() = area
    def release(a: Area) {}
  }
  
}
