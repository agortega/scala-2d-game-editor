package org.brijest.storm.engine
package util
package pathfinding



import model._



case class Path(var lst: List[Direction]) {
  
  def hasNext = lst != Nil
  
  def next(pos: Pos): Pos = lst match {
    case x :: xs =>
      lst = xs
      pos.to(x)
    case Nil => throw new NoSuchElementException
  }
  
}


